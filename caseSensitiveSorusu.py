def to_jaden_case(string):
    string = string.title()
    kelimeler = list(string)
    a = string.find("'", 2)
    kelimeler[a+1] = chr(ord(kelimeler[a+1])+32)
    
    string = ""
    for i in kelimeler:
        string += i
    
    return string

quote = "How can mirrors be real if our eyes aren't real."
print(to_jaden_case(quote))

