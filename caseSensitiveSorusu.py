def a(string):
    a = (string.split(" "))
    b = ""
    for i in a:
        i = i.capitalize()
        b += i + " "
    b += "\b"
    return b

metin = "How can mirrors be real if our eyes aren't real"
print("*" + a(metin)+ "*")

